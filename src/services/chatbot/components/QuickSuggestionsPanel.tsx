/**
 * Componente QuickSuggestionsPanel
 * Muestra sugerencias rápidas que el usuario puede hacer click para enviar
 */
export const QuickSuggestionsPanel: React.FC<QuickSuggestionsPanelProps> = ({
  suggestions,
  onSuggestionClick,
  className = '',
}) => {
  const [displayedSuggestions, setDisplayedSuggestions] =
    useState<QuickSuggestion[]>(suggestions);

  // Actualizar sugerencias cuando cambian
  useEffect(() => {
    setDisplayedSuggestions(suggestions);
  }, [suggestions]);

  // No mostrar si no hay sugerencias
  if (displayedSuggestions.length === 0) {
    return null;
  }

  return (
    <div
      className={`quick-suggestions-panel ${className}`}
      role="region"
      aria-label="Sugerencias rápidas"
    >
      <div className="quick-suggestions-container">
        {displayedSuggestions.map((suggestion) => (
          <button
            key={suggestion.id}
            className="quick-suggestion-btn"
            onClick={() => onSuggestionClick(suggestion)}
            title={suggestion.question}
            aria-label={suggestion.text}
          >
            {suggestion.icon && (
              <span className="quick-suggestion-icon" aria-hidden="true">
                {ICON_MAP[suggestion.icon] || (
                  <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                    <circle cx="12" cy="12" r="10" />
                  </svg>
                )}
              </span>
            )}
            <span className="quick-suggestion-text">{suggestion.text}</span>
          </button>
        ))}
      </div>
    </div>
  );
};

export default QuickSuggestionsPanel;
